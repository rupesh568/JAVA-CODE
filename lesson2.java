class Lesson3{
    public static void main(String[] args){
        int a=33;
        char b='A';
        float c=444.4f;
        double d=555555;
        long e=4433443322L;
        System.out.println("This is integer:" +a);
        System.out.println("This is character:" +b);
        System.out.println("This is a float value:"+c);
        System.out.println("This is a double value:" +d);
        System.out.println("This is a long value:"+e);

    }
    
}