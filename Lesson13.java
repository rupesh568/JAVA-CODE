//Loops :Loops is used to repeat instructions;
//for loop;
public class Lesson13 {
    public static void main(String[] args){
        for(int i=0;i<=5;i++){
            System.out.println("hello world");
        }
    }
}

//While loop;
class First{
    public static void main(String[] args){
        int i=0;
        while(i<=10){
            System.out.println("i="+i);
            i++;
        }
    }
}

//do while loop;
class Hello{
    public static void main(String[] args){
        int i=1;
        do{
            System.out.println("Hello world");
            i++;
        }while(i<=4);
    }
}